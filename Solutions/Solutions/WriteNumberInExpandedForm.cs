namespace Solutions
{
    public class WriteNumberInExpandedForm
    {
        public static string Solution(long num)
        {
            var str = num.ToString();
            return String.Join(" + ", str
                .Select((x, i) => char.GetNumericValue(x) * Math.Pow(10, str.Length - i - 1))
                .Where(x => x > 0));
        }
    }
}
